    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        final int width = 20, height = 20;
        TextCellFactory tcf = new TextCellFactory().font(new Font("Sans Serif", Font.BOLD, 18)).width(18).height(18);
        final SquidPanel display = new SquidPanel(width, height, tcf, null);
        final JPanel jp = new JPanel();
        jp.setBackground(SColor.BLACK);
        jp.add(display);

        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {
                int x = 0, y = 1;
                String input = "";
                display.put(0, 0, "Type input please.");
                display.refresh();
                SquidKey keys = new SquidKey(true, SquidKey.CaptureType.TYPED);
                jp.addKeyListener(keys);

                while (true) {
                    char c = keys.next().getKeyChar();
                    if (c == '\n' || c == '\r') {//cover both windows and unix style line returns
                        display.erase();
                        display.refresh();
                        display.put(0, 0, "Text: " + input);
                        display.refresh();
                        input = "";
                        x = 0;
                        y = 1;
                    } else if (c == '\b') {//backspace character
                        if (input.length() > 0) {
                            input = input.substring(0, input.length() - 1);
                        }
                        if (x > 0) {
                            x--;
                        } else if (y > 1) {
                            y--;
                            x = width - 1;
                        }

                        display.put(x, y, ' ');//clear space
                        display.refresh();
                    } else {
                        input += c;
                        if (x >= width) {
                            x = 0;
                            y++;
                        }

                        if (y < height) {
                            //everything's fine, print the character
                            display.put(x, y, c);
                            x++;
                            display.refresh();
                        }
                    }
                }

            }
        });
        t.setDaemon(true);
        t.start();

        showComponent(jp);
    }//GEN-LAST:event_jButton5ActionPerformed

